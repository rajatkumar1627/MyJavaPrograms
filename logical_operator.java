class logical_operator {
    public static void main(String[] args) {
        int x = 7;
        int y = 6;
        int a = 10;
        int b = 12;
        boolean result = x>y && a>b;
        boolean result1 = x>y || a>b;

        System.out.println(result);
        System.out.println(result1);
    }
}
